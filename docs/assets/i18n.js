export const supportedLanguages = ["en", "fr", "it", "pt", "de", "es"];

export const normalizeLanguage = (language) => {
  const value = String(language || "en").trim().toLowerCase().slice(0, 2);
  return supportedLanguages.includes(value) ? value : "en";
};

